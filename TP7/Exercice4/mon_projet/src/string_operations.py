def concatener(str1, str2):
    """
    Concatène deux chaînes de caractères.

    Parameters:
    str1 (str): La première chaîne de caractères.
    str2 (str): La deuxième chaîne de caractères.

    Returns:
    str: La chaîne de caractères résultante après concaténation.
    """
    return str1 + str2

def mettre_en_majuscule(string):
    """
    Convertit une chaîne de caractères en majuscules.

    Parameters:
    string (str): La chaîne de caractères à convertir.

    Returns:
    str: La chaîne de caractères en majuscules.
    """
    return string.upper()
